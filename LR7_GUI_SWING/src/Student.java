
public class Student {
    private String name;
    private String group;
    private int mark;

    public Student(String name, String group, int mark) {
        this.name = name;
        this.group = group;
        this.mark = mark;
    }

    public String getName() {
        return name;
    }

    public String getGroup() {
        return group;
    }

    public int getMark() {
        return mark;
    }
}

